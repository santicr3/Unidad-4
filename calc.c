#include <stdio.h>

int suma(int a, int b) {

    return a + b;
}

int resta(int a, int b) {

    return a - b;
}

int multiplicacion(int a, int b) {

    return a * b;
}

int division(int a, int b) {

    return a / b;
}

int major(int a, int b){
    if (a > b){
        return a;
    }
    else {
        return b;
    }
}